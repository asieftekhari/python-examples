x=float(input("Enter the first number: "))
y=float(input("Enter the second number: "))

print(f"{x} + {y}  = ", "%.3f"%(x+y))
print(f"{x} - {y}  = ", "%.3f"%(x-y))
print(f"{x} * {y}  = ", "%.3f"%(x*y))
print(f"{x} / {y}  = ", "%.3f"%(x/y))
print(f"{x} ** {y} = ", "%.3f"%(x**y))
print(f"{x} // {y} = ", "%.3f"%(x//y))